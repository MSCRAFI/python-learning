# python program for addition, subtraction, multiplication and division

def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x, y):
    return x / y

# Example usage
print("Addition of 5 and 3:", add(5, 3))          # Output: 8
print("Subtraction of 5 and 3:", subtract(5, 3))  # Output: 2
print("Multiplication of 5 and 3:", multiply(5, 3))  # Output: 15
print("Division of 5 and 3:", divide(5, 3))      # Output: 1.6666666666666667